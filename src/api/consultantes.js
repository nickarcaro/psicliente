import { basePath } from "./config";

// obtener todos los consultantes

export function getConsultants(token) {
  const url = `${basePath}/hechosconsultantes`;

  const params = {
    method: "GET",
    headers: {
      "Content-Type": "application/json",
      Authorization: token,
    },
  };

  return fetch(url, params)
    .then((response) => {
      return response.json();
    })
    .then((result) => {
      return result;
    })
    .catch((err) => {
      return err.message;
    });
}

